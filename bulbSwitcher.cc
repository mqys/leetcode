class Solution {
public:
    int bulbSwitch(int n) {
        // --- 0 ms---
        // have only odd divisor
        // divisor come in pairs, like 12 = 1*12 = 2*6 = 3*4
        // count the perfect square
        // sqrt(n) is the max num whose square is in the range
        return sqrt(n);
        
    }
};